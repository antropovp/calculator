package com.epam.izh.rd.online.exception;

public class DoubleOperatorsException extends Exception {

    public DoubleOperatorsException() {
    }

    public DoubleOperatorsException(String message) {
        super(message);
    }

    public DoubleOperatorsException(String message, Throwable cause) {
        super(message, cause);
    }

    public DoubleOperatorsException(Throwable cause) {
        super(cause);
    }
}
